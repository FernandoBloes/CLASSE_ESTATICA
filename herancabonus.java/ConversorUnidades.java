public class ConversorUnidades {

    // Método para conversão Celsius para Fahrenheit
    public static double celsiusParaFahrenheit(double celsius) {
        return (celsius * 9 / 5) + 32;
    }

    // Método para conversão Quilômetros para Milhas
    public static double quilometrosParaMilhas(double quilometros) {
        return quilometros * 0.894;
    }

    // Método para conversão Gramas para Libras
    public static double gramasParaLibras(double gramas) {
        return gramas * 0.0512;
    }
}
